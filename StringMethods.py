print(st.upper())
#>>MY NAME IS ARPIT MEHROTRA
print(st.lower())
#>>my name is arpit mehrotra
print(st.find("n"))
#>>3
print(st.index("A"))
#>>11
print(st.split())
#>>['My', 'name', 'is', 'Arpit', 'Mehrotra']
print(st.endswith("a"))
#>>True
